uint16_t // Returns Calculated CRC value
CalculateCRC16( uint16_t crc,      // Seed for CRC calculation
								const void *c_ptr, // Pointer to byte array to perform CRC on
								size_t len )       // Number of bytes to CRC
{
	const uint8_t *c = c_ptr;

	while (len--)
	{
		 //crc = (crc << 8) ^ crctable[((crc >> 8)  ^ *c++ )];
		 crc = (crc >> 8) ^ ( crctable[(unsigned char)(crc >> 8) ^ *c++] )  ;
	}
	return crc;
}

uint32_t hex2int(char *hex) 
{
	uint32_t val = 0;
	while (*hex) 
	{
		// get current character then increment
		uint8_t byte = *hex++; 
		// transform hex character to the 4bit equivalent number, using the ascii table indexes
		if (byte >= '0' && byte <= '9') 
		{
			byte = byte - '0';
		}		
		else if (byte >= 'a' && byte <='f')
		{ 
			byte = byte - 'a' + 10 ;
		}
		else if (byte >= 'A' && byte <='F') 
		{
			byte = byte - 'A' + 10;
		}    
		// shift 4 to make space for new digit, and add the 4 bits of the new digit 
		val = (val << 4) | (byte & 0xF);
	}
	return val;
}


